  CREATE OR REPLACE EDITIONABLE PROCEDURE "PANDORA"."PC_ADMINISTRAR_VW_UNIDADE" 
(A_IDS_VW_UNIDADE in AR_ID_NUMBER) as
begin
declare
  cursor PERFIL is select * from PERFIL P;
  RG_PERFIL PERFIL%ROWTYPE;

  ID_PARAMETRO NUMBER;

  NR_CONTADOR_PERFIL NUMBER;

begin
  -- exclui unidade.
  delete from PARAMETRO_VW_UNIDADE;

  -- inclui unidade.
  select P.ID into ID_PARAMETRO from PARAMETRO P;
  for I in A_IDS_VW_UNIDADE.first..A_IDS_VW_UNIDADE.last loop
    insert into PARAMETRO_VW_UNIDADE values (ID_PARAMETRO, A_IDS_VW_UNIDADE(I));
  end loop;

  -- atualiza perfil.
  open PERFIL;
  loop
    fetch PERFIL into RG_PERFIL;
    exit when PERFIL%NOTFOUND;

    for I in A_IDS_VW_UNIDADE.first..A_IDS_VW_UNIDADE.last loop
      select count(*) into NR_CONTADOR_PERFIL from PERFIL_VW_UNIDADE PVWU where PVWU.ID_PERFIL = RG_PERFIL.ID and PVWU.ID_VW_UNIDADE = A_IDS_VW_UNIDADE(I);
      if NR_CONTADOR_PERFIL = 0 then
        insert into PERFIL_VW_UNIDADE values (RG_PERFIL.ID, A_IDS_VW_UNIDADE(I));
      end if;
    end loop;
  end loop;
  close PERFIL;

  commit;
end;

end PC_ADMINISTRAR_VW_UNIDADE;


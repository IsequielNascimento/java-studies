  CREATE OR REPLACE EDITIONABLE FUNCTION "PANDORA"."FC_VERIFICAR_IDS_NUMBER" 
(A_ID_CONTROLE in number, A_IDS_NUMBER in AR_ID_NUMBER, A_TP_CONTROLE in number) return varchar2 is
  -- objetivo: verifica se id pertence a todos os ids informados  
  --     
  -- A_ID_CONTROLE - código de controle
  -- A_IDS_NUMBER - ids a buscar (todos simultaneamente)
  -- A_TP_CONTROLE - tipo de controle
  -- retorno: 'S/N'

  NR_TAMANHO_ARRAY number;
  NR_CONTADOR_CONTROLE number;
begin
  NR_TAMANHO_ARRAY := A_IDS_NUMBER.count;

  if A_TP_CONTROLE = 0 then
    select count(*) into NR_CONTADOR_CONTROLE from SERVICO S where S.ID_HOST = A_ID_CONTROLE and S.ID member of A_IDS_NUMBER;
  elsif A_TP_CONTROLE = 1 then
    select count(*) into NR_CONTADOR_CONTROLE from ESQUEMA E where E.ID_SERVICO = A_ID_CONTROLE and E.ID member of A_IDS_NUMBER;
  elsif A_TP_CONTROLE = 2 then
    select count(*) into NR_CONTADOR_CONTROLE from INSTALACAO I where I.ID_SISTEMA = A_ID_CONTROLE and I.ID_HOST member of A_IDS_NUMBER;
  elsif A_TP_CONTROLE = 3 then
    select count(*) into NR_CONTADOR_CONTROLE from INSTALACAO I where I.ID_SISTEMA = A_ID_CONTROLE and I.ID_SERVICO member of A_IDS_NUMBER;    
  elsif A_TP_CONTROLE = 4 then
    select count(*) into NR_CONTADOR_CONTROLE from INSTALACAO I where I.ID_SISTEMA = A_ID_CONTROLE and I.ID_ESQUEMA member of A_IDS_NUMBER;    
  else
    select count(*) into NR_CONTADOR_CONTROLE from FUNCIONALIDADE_TIPO FT where FT.ID_FUNCIONALIDADE = A_ID_CONTROLE and FT.ID_TIPO_FUNCIONALIDADE member of A_IDS_NUMBER;
  end if;

  if NR_CONTADOR_CONTROLE < NR_TAMANHO_ARRAY then
    return 'N';
